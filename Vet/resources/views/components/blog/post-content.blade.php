                    <p>
                        Annual wellness exams are the single most important action you can take to ensure your pet lives a long, healthy, and happy life. Unlike humans who visit the doctor when they feel ill, pets cannot communicate their discomfort, and their instincts often lead them to hide symptoms of disease until the condition is advanced.
                    </p>
                    
                    <h2>Why Annual Visits Matter</h2>
                    <p>
                        A yearly exam allows our VET team to establish a baseline for your pet's normal health. This benchmark helps us quickly identify subtle changes in weight, organ function, or behavior during subsequent visits.
                    </p>

                    <h3 class="text-2xl font-bold text-primary-700 mt-6 mb-3">What the Comprehensive Exam Covers:</h3>
                    
                    <ul>
                        <li>**Full Physical Assessment:** Checking eyes, ears, skin, coat, heart, lungs, and abdomen.</li>
                        <li>**Dental Health Check:** Assessing the oral cavity for signs of periodontal disease.</li>
                        <li>**Vaccination Review:** Customizing a vaccine schedule based on your pet’s lifestyle and risk.</li>
                        <li>**Parasite Prevention:** Discussing year-round protection against fleas, ticks, and heartworm.</li>
                        <li>**Nutritional Guidance:** Recommendations tailored to your pet’s age, breed, and activity level.</li>
                    </ul>

                    <blockquote>
                        "Early diagnosis of diseases like kidney failure, diabetes, and arthritis can drastically improve the treatment outcome and prolong your pet’s quality of life."
                    </blockquote>
                    
