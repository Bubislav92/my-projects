            <button id="next-btn" class="text-white text-5xl opacity-50 hover:opacity-100 transition-opacity">&rsaquo;</button>
        </div>
        
        <div class="absolute bottom-10 text-white text-center text-lg z-50">
            <span id="image-caption"></span>
