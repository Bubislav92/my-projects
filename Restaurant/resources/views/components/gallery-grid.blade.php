<script>
    document.addEventListener('DOMContentLoaded', function() {
        const galleryItems = document.querySelectorAll('.gallery-item');
        const modal = document.getElementById('gallery-modal');
        const modalImage = document.getElementById('modal-image');
        const closeBtn = document.getElementById('close-modal');
        const prevBtn = document.getElementById('prev-btn');
        const nextBtn = document.getElementById('next-btn');
        const imageCaption = document.getElementById('image-caption');

        let currentImages = [];
        let currentIndex = 0;

        // Open Modal
        galleryItems.forEach(item => {
            item.addEventListener('click', function(e) {
                e.preventDefault();
                
                const category = this.getAttribute('data-category');
                currentIndex = parseInt(this.getAttribute('data-index'));

                // Filter images by category
                currentImages = Array.from(document.querySelectorAll(`[data-category="${category}"]`)).map(el => el.querySelector('img'));
                
                updateModalContent();
                modal.classList.remove('hidden');
            });
        });

        // Update Modal Content
        function updateModalContent() {
            if (currentImages.length > 0) {
                const currentImg = currentImages[currentIndex];
                modalImage.src = currentImg.src;
                imageCaption.textContent = currentImg.alt;
            }
        }

        // Close Modal
        closeBtn.addEventListener('click', function() {
            modal.classList.add('hidden');
        });

        // Navigate with Prev/Next buttons
        prevBtn.addEventListener('click', function() {
            currentIndex = (currentIndex - 1 + currentImages.length) % currentImages.length;
            updateModalContent();
        });

        nextBtn.addEventListener('click', function() {
            currentIndex = (currentIndex + 1) % currentImages.length;
            updateModalContent();
        });

        // Close modal by clicking outside
        modal.addEventListener('click', function(e) {
            if (e.target === modal) {
                modal.classList.add('hidden');
            }
        });

        // Close modal with Escape key
        document.addEventListener('keydown', function(e) {
            if (e.key === 'Escape' && !modal.classList.contains('hidden')) {
                modal.classList.add('hidden');
            }
        });
    });
</script>